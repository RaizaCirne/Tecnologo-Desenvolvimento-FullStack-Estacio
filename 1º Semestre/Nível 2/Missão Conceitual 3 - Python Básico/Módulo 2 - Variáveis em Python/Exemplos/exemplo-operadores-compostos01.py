x = 10             # x recebe o valor 10
print(f'x={x}')    # Imprimir o valor de x, que é o 10
x+=2               # Incrementar o X de 2, ou seja, somar 2 ao valor de X usando o operador composto +=
print(f'x={x}')    # Imprimir valor de x que agora é 12
x-=2               # Tirar 2 de X, ou seja, subtrair
print(f"x={x}")    # Ao imprimir temos X = 10