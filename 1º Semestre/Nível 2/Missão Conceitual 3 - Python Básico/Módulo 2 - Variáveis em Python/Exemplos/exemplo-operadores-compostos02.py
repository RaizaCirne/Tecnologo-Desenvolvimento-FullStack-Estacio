x = 10             # x recebe 10
x*=2               # Multiplicar o valor de x por 2
print(f"x={x}")    # O resultado na tela é 20
x/=2               # Divido o valor de x por 2
print(f"x={x}")    # 20/ 2 dá 10
x%=2               # Calcular o resto da divisão inteira de x por 2
print(f"x{x}")     # 10/ 2 = 5 e o resto da divisão é zero. A tela mostra 0